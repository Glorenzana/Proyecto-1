    private void btnImprimirFactMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnImprimirFactMouseClicked
        // TODO add your handling code here:
        String mensaje = "";
        if (this.txtNombreCli.getText().equals("") || this.txtDireccionCli.getText().equals("")) {
            mensaje = "Asegurese de colocar Nombre o Direcci√≥n";
            mensajeError(mensaje);
        } else if ((this.cmbFormaPago.getSelectedItem() == "Credito") && (this.txtNoCuenta.getText().equals(""))) {
            mensaje = "Por favor ingrese el No. de cuenta del cliente";
            mensajeError(mensaje);
        } else if ((this.cmbFormaPago.getSelectedItem() == "Cheque") && this.txtNoCheque.getText().equals("")) {
            mensaje = "Por favor ingrese el No. de cheque del cliente";
            mensajeError(mensaje);
        } else {
            if (this.existeCliente) {
                this.agregarFactura();
            } else {
                this.agregarCliente();
                this.agregarFactura();
            }
        }
    }//GEN-LAST:event_btnImprimirFactMouseClicked

    private void mensajeError(String mensaje) {
        JOptionPane.showMessageDialog(null,
                mensaje,
                "Error",
                JOptionPane.ERROR_MESSAGE);
    }

    private void agregarCliente() {
        ArrayList<Cliente> clientes = new ArrayList<Cliente>();
        Cliente nuevoCliente = new Cliente();
        nuevoCliente.setNombre(this.txtNombreCli.getText().toString());
        nuevoCliente.setDireccion(this.txtDireccionCli.getText().toString());
        nuevoCliente.setNit(this.txtNitCli.getText().toString());
        if (this.cmbFormaPago.getSelectedItem() == "Credito") {
            nuevoCliente.setDeudaCredito(totalFactura);
        } else {
            nuevoCliente.setDeudaCredito(0.0);
        }
        clientes.add(nuevoCliente);
        if (this.existeArchivoCliente) {
            ControlCliente.agregarCliente(clientes);
        } else {
            ControlCliente.crearArchivo(clientes);
        }

    }

    private void agregarFactura() {
        Factura factura = new Factura();
        factura.setNit(this.txtNitCli.getText().toString());
        factura.setFormaPago(this.cmbFormaPago.getSelectedItem().toString());
        factura.setTotal(totalFactura);
        this.listaFacturas.add(factura);
        if (this.existArhivoFactura()) {
            ControlFactura.agregarFactura(listaFacturas);
        } else {
            ControlFactura.agregarFactura(listaFacturas);
        }
    }

