    private void cmbFormaPagoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbFormaPagoItemStateChanged
        // TODO add your handling code here:
        if (this.cmbFormaPago.getSelectedItem() == "Credito") {
            if (this.totalFactura > credito || (this.existeCliente && this.saldoDelCliente < this.totalFactura)) {
                String mensaje ="Cr√©dito insuficiente...";
                mensajeError(mensaje);
                this.cmbFormaPago.remove("Credito");
            } else {
                this.txtNoCuenta.setText("");
                this.txtNoCuenta.setEnabled(true);
                this.txtNoCuenta.setEditable(true);
                this.txtNoCheque.setEnabled(false);
            }

        } else if (this.cmbFormaPago.getSelectedItem() == "Cheque") {
            this.txtNoCheque.setEnabled(true);
            this.txtNoCheque.setEditable(true);
            this.txtNoCuenta.setEnabled(false);
        } else {
            this.txtNoCheque.setEnabled(false);
            this.txtNoCuenta.setEnabled(false);
        }
    }//GEN-LAST:event_cmbFormaPagoItemStateChanged

