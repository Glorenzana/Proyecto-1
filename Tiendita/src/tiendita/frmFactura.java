    public void configuracionInicial(String Nit, double totalDetalle, DefaultListModel detalleFactura) {
        this.totalFactura = 0.0;
        this.txtNitCli.setText(Nit);
        this.jListDetalleFactura.setModel(detalleFactura);
        consultarExisteCliente(Nit);
        totalFactura = totalDetalle;
        this.txtTotal.setText(String.valueOf(totalFactura));
    }

    private void consultarExisteCliente(String Nit) {
        listaClientes = ControlCliente.leerArchivo();
        if (listaClientes.isEmpty()) {
            this.lblMensajeAdvertencia.setVisible(true);
            this.existeArchivoCliente = false;
        } else {
            this.existeArchivoCliente = true;
            this.cliente = buscarCliente(Nit);
            if (this.cliente != null) {
                saldoDelCliente = credito - this.cliente.getDeudaCredito();
                this.existeCliente = true;
                this.txtNombreCli.setText(this.cliente.getNombre());
                this.txtNombreCli.setEnabled(false);
                this.txtDireccionCli.setText(this.cliente.getDireccion());
                this.txtDireccionCli.setEnabled(false);
                this.lblSaldoRestante.setText("Credito: Q " + String.valueOf(saldoDelCliente));
            } else {
                this.existeCliente = false;
            }
        }

    }

    private Cliente buscarCliente(String nit) {
        Cliente filtroCliente = listaClientes.stream()
                .filter(cli -> cli.getNit().equals(nit))
                .findAny()
                .orElse(null);
        return filtroCliente;
    }

    private boolean existArhivoFactura() {
        ArrayList<Factura> facturas = ControlFactura.leerArchivo();
        if (facturas.isEmpty()) {
            return false;
        } else {
            return true;
        }
    }

